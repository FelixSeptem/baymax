func shouldAutoGenerateDiagnosticsBundle(
	manager *runtimeconfig.Manager,
	bundleTotal int,
	bundleStatus string,
	bundleSchemaVersion string,
	bundleReasonCode string,
) bool {
	if manager == nil {
		return false
	}
	cfg := manager.EffectiveConfig().Runtime.Diagnostics.Bundle
	if !cfg.Enabled {
		return false
	}
	if bundleTotal > 0 {
		return false
	}
	if strings.TrimSpace(bundleStatus) != "" {
		return false
	}
	if strings.TrimSpace(bundleSchemaVersion) != "" {
		return false
	}
	if strings.TrimSpace(bundleReasonCode) != "" {
		return false
	}
	return true
}

