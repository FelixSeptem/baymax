type RuntimeRecorder struct {
	manager         *runtimeconfig.Manager
	exporterRuntime *runtimeExporterRuntime
}

type RuntimeRecorderOption func(*runtimeRecorderOptionSet)

type runtimeRecorderOptionSet struct {
	exporterResolver RuntimeExporterResolver
	exporterFactory  map[string]RuntimeExporterFactory
}

func WithRuntimeExporterResolver(resolver RuntimeExporterResolver) RuntimeRecorderOption {
	return func(opts *runtimeRecorderOptionSet) {
		if opts == nil || resolver == nil {
			return
		}
		opts.exporterResolver = resolver
	}
}

func WithRuntimeExporterFactory(profile string, factory RuntimeExporterFactory) RuntimeRecorderOption {
	return func(opts *runtimeRecorderOptionSet) {
		if opts == nil || factory == nil {
			return
		}
		normalized := strings.ToLower(strings.TrimSpace(profile))
		if normalized == "" {
			return
		}
		if opts.exporterFactory == nil {
			opts.exporterFactory = map[string]RuntimeExporterFactory{}
		}
		opts.exporterFactory[normalized] = factory
	}
}

func NewRuntimeRecorder(manager *runtimeconfig.Manager, options ...RuntimeRecorderOption) *RuntimeRecorder {
	opts := runtimeRecorderOptionSet{
		exporterFactory: map[string]RuntimeExporterFactory{},
	}
	for i := range options {
		if options[i] == nil {
			continue
		}
		options[i](&opts)
	}
	resolver := opts.exporterResolver
	if resolver == nil {
		resolver = newDefaultRuntimeExporterResolver(opts.exporterFactory)
	}
	return &RuntimeRecorder{
		manager:         manager,
		exporterRuntime: newRuntimeExporterRuntime(resolver),
	}
}

func (r *RuntimeRecorder) ExportSnapshot() RuntimeExportSnapshot {
	if r == nil || r.exporterRuntime == nil {
		return RuntimeExportSnapshot{}
	}
	return r.exporterRuntime.Snapshot()
}

func (r *RuntimeRecorder) Close() {
	if r == nil || r.exporterRuntime == nil {
		return
	}
	r.exporterRuntime.Close()
}

