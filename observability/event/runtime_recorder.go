func payloadAnyMap(m map[string]any, key string) map[string]any {
	if len(m) == 0 {
		return nil
	}
	raw, ok := m[key]
	if !ok || raw == nil {
		return nil
	}
	switch value := raw.(type) {
	case map[string]any:
		return cloneAnyMap(value)
	default:
		blob, err := json.Marshal(raw)
		if err != nil {
			return nil
		}
		var decoded map[string]any
		if err := json.Unmarshal(blob, &decoded); err != nil {
			return nil
		}
		return cloneAnyMap(decoded)
	}
}

type inferentialAdvisory struct {
	Status  string
	Score   float64
	Signals map[string]float64
	Reasons []string
}

func buildInferentialAdvisory(
	evalCfg runtimeconfig.RuntimeEvalAgentConfig,
	evalSummary map[string]any,
	readinessStatus string,
	budgetDecision string,
	decision string,
) inferentialAdvisory {
	signals := map[string]float64{}
	reasonSet := map[string]struct{}{}
	addSignal := func(key string, value float64) {
		key = strings.TrimSpace(key)
		if key == "" {
			return
		}
		signals[key] = clampInferentialSignal(value)
	}
	addReason := func(code string) {
		code = strings.TrimSpace(code)
		if code == "" {
			return
		}
		reasonSet[code] = struct{}{}
	}

	if rate, ok := evalSummaryMetricRate(evalSummary, "task_success"); ok {
		addSignal("task_success_rate", rate)
		if evalCfg.Enabled && rate < evalCfg.TaskSuccessThreshold {
			addReason("eval.task_success_below_threshold")
		}
	}
	if rate, ok := evalSummaryMetricRate(evalSummary, "tool_correctness"); ok {
		addSignal("tool_correctness_rate", rate)
		if evalCfg.Enabled && rate < evalCfg.ToolCorrectnessThreshold {
			addReason("eval.tool_correctness_below_threshold")
		}
	}
	if rate, ok := evalSummaryMetricRate(evalSummary, "deny_intercept"); ok {
		addSignal("deny_intercept_rate", rate)
		if evalCfg.Enabled && rate < evalCfg.DenyInterceptAccuracyThreshold {
			addReason("eval.deny_intercept_below_threshold")
		}
	}
	if within, ok := evalSummaryCostLatencyBool(evalSummary, "cost_within_budget"); ok {
		if within {
			addSignal("cost_within_budget", 1)
		} else {
			addSignal("cost_within_budget", 0)
			if evalCfg.Enabled {
				addReason("eval.cost_budget_exceeded")
			}
		}
	}
	if within, ok := evalSummaryCostLatencyBool(evalSummary, "latency_within_slo"); ok {
		if within {
			addSignal("latency_within_slo", 1)
		} else {
			addSignal("latency_within_slo", 0)
			if evalCfg.Enabled {
				addReason("eval.latency_budget_exceeded")
			}
		}
	}

	normalizedReadiness := strings.ToLower(strings.TrimSpace(readinessStatus))
	switch normalizedReadiness {
	case "degraded":
		addSignal("runtime_readiness_degraded", 1)
		addReason("runtime.readiness.degraded")
	case "blocked":
		addSignal("runtime_readiness_blocked", 1)
		addReason("runtime.readiness.blocked")
	}

	normalizedBudgetDecision := strings.ToLower(strings.TrimSpace(budgetDecision))
	switch normalizedBudgetDecision {
	case string(runtimeconfig.RuntimeAdmissionBudgetDecisionDeny):
		addSignal("runtime_admission_budget_deny", 1)
		addReason("runtime.admission.budget_deny")
	case string(runtimeconfig.RuntimeAdmissionBudgetDecisionDegrade):
		addSignal("runtime_admission_budget_degrade", 1)
		addReason("runtime.admission.budget_degrade")
	}

	normalizedDecision := strings.ToLower(strings.TrimSpace(decision))
	if normalizedDecision == "deny" {
		addSignal("runtime_policy_decision_deny", 1)
		addReason("runtime.policy.deny")
	}

	score := 1.0
	if len(signals) > 0 {
		total := 0.0
		for _, value := range signals {
			total += clampInferentialSignal(value)
		}
		score = total / float64(len(signals))
	}

	reasons := make([]string, 0, len(reasonSet))
	for code := range reasonSet {
		reasons = append(reasons, code)
	}
	sort.Strings(reasons)
	if len(reasons) == 0 {
		reasons = nil
	}
	if len(signals) == 0 {
		signals = nil
	}

	var status string
	switch {
	case len(reasons) == 0:
		status = "healthy"
	case normalizedDecision == "deny" || normalizedBudgetDecision == string(runtimeconfig.RuntimeAdmissionBudgetDecisionDeny) || normalizedReadiness == "blocked":
		status = "critical"
	default:
		status = "watch"
	}

	return inferentialAdvisory{
		Status:  status,
		Score:   clampInferentialSignal(score),
		Signals: signals,
		Reasons: reasons,
	}
}

func evalSummaryMetricRate(evalSummary map[string]any, metric string) (float64, bool) {
	metricMap, ok := evalSummaryChildMap(evalSummary, metric)
	if !ok {
		return 0, false
	}
	if value, ok := metricMap["rate"]; ok {
		if parsed, ok := anyFloat64(value); ok {
			return clampInferentialSignal(parsed), true
		}
	}
	if value, ok := metricMap["pass_rate"]; ok {
		if parsed, ok := anyFloat64(value); ok {
			return clampInferentialSignal(parsed), true
		}
	}
	return 0, false
}

func evalSummaryCostLatencyBool(evalSummary map[string]any, key string) (bool, bool) {
	costLatency, ok := evalSummaryChildMap(evalSummary, "cost_latency")
	if !ok {
		return false, false
	}
	raw, ok := costLatency[key]
	if !ok {
		return false, false
	}
	return anyBool(raw)
}

func evalSummaryChildMap(evalSummary map[string]any, key string) (map[string]any, bool) {
	if len(evalSummary) == 0 {
		return nil, false
	}
	raw, ok := evalSummary[key]
	if !ok || raw == nil {
		return nil, false
	}
	typed, ok := raw.(map[string]any)
	if !ok {
		return nil, false
	}
	return typed, true
}

func anyFloat64(raw any) (float64, bool) {
	switch value := raw.(type) {
	case float64:
		return value, true
	case float32:
		return float64(value), true
	case int:
		return float64(value), true
	case int8:
		return float64(value), true
	case int16:
		return float64(value), true
	case int32:
		return float64(value), true
	case int64:
		return float64(value), true
	case uint:
		return float64(value), true
	case uint8:
		return float64(value), true
	case uint16:
		return float64(value), true
	case uint32:
		return float64(value), true
	case uint64:
		return float64(value), true
	case json.Number:
		parsed, err := value.Float64()
		if err != nil {
			return 0, false
		}
		return parsed, true
	default:
		return 0, false
	}
}

func anyBool(raw any) (bool, bool) {
	switch value := raw.(type) {
	case bool:
		return value, true
	case string:
		switch strings.ToLower(strings.TrimSpace(value)) {
		case "true":
			return true, true
		case "false":
			return false, true
		default:
			return false, false
		}
	default:
		return false, false
	}
}

func clampInferentialSignal(value float64) float64 {
	switch {
	case value < 0:
		return 0
	case value > 1:
		return 1
	default:
		return value
	}
}

func cloneAnyMap(in map[string]any) map[string]any {
	if len(in) == 0 {
		return nil
	}
	out := make(map[string]any, len(in))
	for key, value := range in {
		if child, ok := value.(map[string]any); ok {
			out[key] = cloneAnyMap(child)
			continue
		}
		out[key] = value
	}
	return out
}

func payloadStringSlice(m map[string]any, key string) []string {
	if len(m) == 0 {
		return nil
	}
	raw, ok := m[key]
	if !ok {
		return nil
	}
	out := []string{}
	switch src := raw.(type) {
	case []string:
		for i := range src {
			item := strings.TrimSpace(src[i])
			if item == "" {
				continue
			}
			out = append(out, item)
		}
	case []any:
		for i := range src {
			item, ok := src[i].(string)
			if !ok {
				continue
			}
			item = strings.TrimSpace(item)
			if item == "" {
				continue
			}
			out = append(out, item)
		}
	default:
		return nil
	}
	if len(out) == 0 {
		return nil
	}
	return out
}

func payloadPolicyDecisionPath(m map[string]any, key string) []runtimediag.RuntimePolicyDecisionPathEntry {
	if len(m) == 0 {
		return nil
	}
	raw, ok := m[key]
	if !ok {
		return nil
	}
	out := make([]runtimediag.RuntimePolicyDecisionPathEntry, 0)
	appendItem := func(stage, code, source, decision string) {
		entry := runtimediag.RuntimePolicyDecisionPathEntry{
			Stage:    strings.ToLower(strings.TrimSpace(stage)),
			Code:     strings.TrimSpace(code),
			Source:   strings.ToLower(strings.TrimSpace(source)),
			Decision: strings.ToLower(strings.TrimSpace(decision)),
		}
		if entry.Stage == "" {
			return
		}
		out = append(out, entry)
	}
	switch src := raw.(type) {
	case []runtimeconfig.RuntimePolicyCandidate:
		for i := range src {
			appendItem(src[i].Stage, src[i].Code, src[i].Source, src[i].Decision)
		}
	case []runtimediag.RuntimePolicyDecisionPathEntry:
		for i := range src {
			appendItem(src[i].Stage, src[i].Code, src[i].Source, src[i].Decision)
		}
	case []any:
		for i := range src {
			switch item := src[i].(type) {
			case map[string]any:
				appendItem(
					payloadString(item, "stage"),
					payloadString(item, "code"),
					payloadString(item, "source"),
					payloadString(item, "decision"),
				)
			case runtimeconfig.RuntimePolicyCandidate:
				appendItem(item.Stage, item.Code, item.Source, item.Decision)
			case runtimediag.RuntimePolicyDecisionPathEntry:
				appendItem(item.Stage, item.Code, item.Source, item.Decision)
			}
		}
	default:
		return nil
	}
	if len(out) == 0 {
		return nil
	}
	return out
}

func payloadStringSliceCSV(m map[string]any, key string) []string {
	if len(m) == 0 {
		return nil
	}
	raw, ok := m[key]
	if !ok {
		return nil
	}
	switch src := raw.(type) {
	case string:
		parts := strings.Split(src, ",")
		out := make([]string, 0, len(parts))
		for i := range parts {
			item := strings.TrimSpace(parts[i])
			if item == "" {
				continue
			}
			out = append(out, item)
		}
		if len(out) == 0 {
			return nil
		}
		return out
	default:
		return payloadStringSlice(m, key)
	}
}

func payloadOrDefault(m map[string]any, key, fallback string) string {
	v := payloadString(m, key)
	if v == "" {
		return fallback
	}
	return v
}

func payloadStringOrFallback(m map[string]any, key, fallback string) string {
	v := payloadString(m, key)
	if v == "" {
		return strings.TrimSpace(fallback)
	}
	return v
}

func payloadIntOrFallback(m map[string]any, key string, fallback int) int {
	if len(m) == 0 {
		return fallback
	}
	if _, ok := m[key]; !ok {
		return fallback
	}
	return payloadInt(m, key)
}

func payloadBool(m map[string]any, key string) bool {
	if len(m) == 0 {
		return false
	}
	raw, ok := m[key]
	if !ok {
		return false
	}
	v, _ := raw.(bool)
	return v
}

func payloadBoolCompat(m map[string]any, primary string, legacy string) bool {
	if payloadHasKey(m, primary) {
		return payloadBool(m, primary)
	}
	return payloadBool(m, legacy)
}

